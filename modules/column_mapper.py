# обратный индекс: нормализованный алиас -> каноническое имя
_ALIAS_INDEX: dict[str, str] = {}
for canon, variants in ALIASES.items():
    for v in variants:
        _ALIAS_INDEX[v] = canon


def _norm(s: str) -> str:
    s = str(s or "").lower().strip()
    s = s.replace("ё", "е")
    s = re.sub(r"[\s_]+", " ", s)
    s = re.sub(r"[.,;:()%\"'«»]+", "", s)
    return s.strip()


def map_column_name(col: str, threshold: float = 0.82) -> tuple[str, float, str]:
    """
    Возвращает (canonical_name, confidence, method) для одной колонки.
    Если сопоставить не удалось — canonical_name = "extra__<исходное имя>",
    confidence = 0.0, method = "unmatched".
    """
    norm = _norm(col)
    if not norm:
        return f"extra__{col}", 0.0, "empty"

    # точное совпадение
    if norm in _ALIAS_INDEX:
        return _ALIAS_INDEX[norm], 1.0, "exact"

    # подстрочное совпадение (алиас входит в название колонки или наоборот)
    best_canon, best_score = None, 0.0
    for alias, canon in _ALIAS_INDEX.items():
        if alias in norm or norm in alias:
            score = len(alias) / max(len(norm), 1)
            score = min(score, 0.99)
            if score > best_score:
                best_score, best_canon = score, canon
    if best_canon and best_score >= 0.5:
        return best_canon, best_score, "substring"

    # нечёткое совпадение (опечатки, вариации)
    aliases = list(_ALIAS_INDEX.keys())
    matches = difflib.get_close_matches(norm, aliases, n=1, cutoff=threshold)
    if matches:
        canon = _ALIAS_INDEX[matches[0]]
        ratio = difflib.SequenceMatcher(None, norm, matches[0]).ratio()
        return canon, ratio, "fuzzy"

    return f"extra__{col}", 0.0, "unmatched"


def map_dataframe_columns(df: pd.DataFrame) -> tuple[pd.DataFrame, list[dict]]:
    """
    Переименовывает колонки DataFrame в канонические имена.
    Возвращает (новый df, отчёт по каждой колонке).
    Если несколько исходных колонок сопоставляются в одно каноническое имя
    (например, два поля "ИИН/БИН" для поставщика и покупателя в ЭСФ),
    вызывающий код (esf_parser) должен разруливать это по позиции —
    здесь применяется наивное переименование "в лоб" плюс дедупликация.
    """
    report = []
    new_cols = []
    seen: dict[str, int] = {}
    for col in df.columns:
        canon, conf, method = map_column_name(col)
        if canon in seen:
            seen[canon] += 1
            final_name = f"{canon}__{seen[canon]}"
        else:
            seen[canon] = 0
            final_name = canon
        new_cols.append(final_name)
        report.append(
            {
                "original": col,
                "canonical": canon,
                "confidence": round(conf, 2),
                "method": method,
            }
        )
    out = df.copy()
    out.columns = new_cols
    return out, report
